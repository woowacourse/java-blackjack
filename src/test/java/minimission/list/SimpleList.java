package minimission.list;

public interface SimpleList<E> {

    boolean add(E value);

    void add(int index, E value);
    
    E set(int index, E value);
    
    E get(int index);

    boolean contains(E value);

    int indexOf(E value);

    int size();

    boolean isEmpty();

    boolean remove(E value);
    
    E remove(int index);

    void clear();
}
